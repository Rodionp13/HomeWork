func partOne() {
    print("Hey angel Do you know the reasons why We look up to the sky?")
    print("Hey angel Do you look at us and laugh When we hold on to the past? Hey angel")
}

func signOfDivision() {
    print("-------~-------")
}

func chorus() {
    signOfDivision()
    print("Oh I wish I could be more like you Do you wish you could be more like me?")
    print("Oh I wish I could be more like you Do you wish you could be more like me?")
    signOfDivision()
}

func partTwo() {
    print("Hey angel Tell me, do you ever try To come to the other side?")
    print("Hey angel Tell me, do you ever cry When we waste away our lives?")
}


func theEntireSong() {
    partOne()
    chorus()
    partTwo()
    chorus()
}

theEntireSong()






